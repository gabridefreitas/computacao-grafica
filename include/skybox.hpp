class Skybox {
public:
	unsigned int VAO, VBO;

	Skybox() {
		glGenVertexArrays(1, &VAO);
		glGenBuffers(1, &VBO);
		glBindVertexArray(VAO);
		glBindBuffer(GL_ARRAY_BUFFER, VBO);
		glBufferData(GL_ARRAY_BUFFER, sizeof(skybox_data::skyboxVertices),
				&skybox_data::skyboxVertices, GL_STATIC_DRAW);
		glEnableVertexAttribArray(0);
		glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);
		glBindVertexArray(0);
	}

	void draw() {
		glDepthFunc(GL_LEQUAL);
		glBindVertexArray(VAO);
		glDrawArrays(GL_TRIANGLES, 0, sizeof(skybox_data::skyboxVertices) / sizeof(float));
		glDepthFunc(GL_LESS);
	}
};

